package exe3;

public class Pessoa {String nome;
    int idade;
    int cpf;
    double salario;
    double valorEmprestimo;

    public Pessoa(String nome, int idade, int cpf, double salario, double valorEmprestimo) {
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
        this.salario = salario;
        this.valorEmprestimo = valorEmprestimo;
    }
}
